		public void ShuffleGenericList(List<string[]> list)
		{
			Random random = new Random();
			int i = list.Count;
			while (i > 1)
			{
				i--;
				int index = random.Next(i + 1);
				string[] value = list[index];
				list[index] = list[i];
				list[i] = value;
			}
		}

